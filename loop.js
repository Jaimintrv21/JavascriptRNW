let i=2;
let a = 9;
//Entry controlled loop
while(i<=10){
    document.write("I is ",i,"<br>");
    i+=2;
}
document.write("=====================================<br>");
//Exit controlled loop
do{
    document.write("A is ",a,"<br>");
    a++;
}while(a!=10);
document.write("=====================================<br>");
for(let b = 20;b<=25;b++){
    document.write("B is ",b,"<br>");
}
